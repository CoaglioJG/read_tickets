import { getBarCode } from '../shared/utils/getBarCode';

describe('GetBarCode testing', () => {
  test('It should return for digitable line 21290001192110001210504475617405975870000002000 of length 47 this string "21299758700000020000001121100012100447561740" )', () => {
    expect(
      getBarCode('21290001192110001210504475617405975870000002000', 47),
    ).toBe('21299758700000020000001121100012100447561740');
  });
  test('It should return for digitable line 212900011921100012105044756174059758700000020000 of length 48 this string "21290001192100012105047561740597570000002000" )', () => {
    expect(
      getBarCode('212900011921100012105044756174059758700000020000', 48),
    ).toBe('21290001192100012105047561740597570000002000');
  });
});
